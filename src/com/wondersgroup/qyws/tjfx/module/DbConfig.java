package com.wondersgroup.qyws.tjfx.module;
/**
 * 数据库配置
 * @author Administrator
 *
 */
public class DbConfig {
	
	private String qdm;
	
	private String url;
	
	private String yhm;
	
	private String mm;

	public String getQdm() {
		return qdm;
	}

	public void setQdm(String qdm) {
		this.qdm = qdm;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getYhm() {
		return yhm;
	}

	public void setYhm(String yhm) {
		this.yhm = yhm;
	}

	public String getMm() {
		return mm;
	}

	public void setMm(String mm) {
		this.mm = mm;
	}
}
